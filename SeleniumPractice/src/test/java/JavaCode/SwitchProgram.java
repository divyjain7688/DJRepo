package JavaCode;

public class SwitchProgram {
public static void main(String arg[])
{
	int number = 20;
	switch(number)
	{
		case 10:
			System.out.println("number is 10 ");
			break;
		case 20:
			System.out.println("numbr is 20 ");
			break;
		case 30:
			System.out.println("number is 30 ");
			break;
			default:
			System.out.println("invalid number");
			break;
	}
}

}
